package ebac_Mod11;
import java.util.*;


public class Exercicio {




    public static void main(String[] args) {

        Scanner s = new Scanner(System.in);

        int num = s.nextInt();


        String[] nomes = new String[num];


        for(int i=0; i <= num; i++) {



            Scanner n = new Scanner(System.in);
            String inpt = n.nextLine();

            nomes[i] = inpt;

        }
    }








}
